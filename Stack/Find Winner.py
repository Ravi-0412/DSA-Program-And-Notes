"""
Q) Each person's rank is given, smaller rank defeats the higher rank. 2 players are playing in one match. 
Display the matches being played. 

Example -

For input this - [5 3 1 8 4 6 2 7]
Output –
5 3 1 8 4 6 2 7
3 1 4 2
1 2
1

Note : This will only work if total matches are in power of '2'. 

Time Complexity: O(N). Although it looks like nested loops, the total number of matches played in a tournament is N - 1.
Space Complexity: O(N) to store the rounds, or O(1) if you modify the list in place (though O(N) is safer for clarity).
"""

def simulate_tournament(ranks):
    # Thought: Continue until only one winner remains
    while len(ranks) >= 1:
        # Step 1: Display current round participants
        print(*(ranks))
        
        # Termination: If we just printed the final winner, stop
        if len(ranks) == 1:
            break
            
        next_round = []
        # Step 2: Pair up adjacent players (0&1, 2&3, etc.)
        for i in range(0, len(ranks), 2):
            player1 = ranks[i]
            player2 = ranks[i+1]
            
            # Logic: Smaller rank defeats higher rank
            winner = min(player1, player2)
            next_round.append(winner)
        
        # Step 3: Update ranks for the next round iteration
        ranks = next_round

